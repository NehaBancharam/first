				<h3 class="h3style text-center">What is Anse-La-Raie like?</h3>
				<p class="lead content">During fieldwork, 47 species of fish and 18 species of coral were identified.
							The study area is not homogeneous and has patch with distinct coral species. We distinguish two types of areas “lettuce-type” corals and “mixed” corals such branching corals, leafy corals.  This mapping is being used for the development of monitoring methodologies that will begin during the second half of 2012.</p><br><br>
				
				<h3 class="h3style text-center">Next steps</h3>
				<p class="lead content">This first study provides an approximation of the distribution of species, but it does not replace a comprehensive quantitative study using transects and quadrats. Such a study will be carried out during the second half of 2012. The main objectives of this study are to:</p>
			
			<h3 class="h3style text-center">1. Assess the health of coral reefs looking for diversity and abundance of corals and to monitor the water quality in the lagoon</h3>
				<p class="lead content"> Inventories of the distribution of corals in the zone,
Inventories on the recovery of corals and their health
Inventory of invertebrate fauna (echinoderms, molluscs, crustaceans)
Inventory of fish
Studies on the sedimentation of the lagoon, current in the lagoon
Studies on users of the lagoon of Anse La Raie
Surveys on different physical parameters (T ° C, pH, salinity, turbidity)</p>
			
			<h3 class="h3style text-center">2. Assess the reproductive capacity of existing reefs</h3>
				<p class="lead content">Studies on the recruitment of coral larvae</p>
			
			<h3 class="h3style text-center">3. Determine which coral species can be used for the rehabilitation of reefs in Anse La Raie</h3>
				<p class="lead content">Experiment 1: Growth of corals with different levels of sedimentation<br/>
										Experiment 2: Experiment with feeding and sedimentation
										Experiment 3: Experiment with feeding and on the stress caused to corals by temperature</p>
				
				<h3 class="h3style text-center">4. Provide a basis for education and awareness programmes</h3>
				<p class="lead content">Present the results of the study and monitoring to students, staff of the hotel and visitors at the Nauticaz marine laboratory.
Organize an open house for press and media on the scientific research on the rehabilitation of coral reefs in the lagoon of Anse La Raie.
Publish results in scientific journals.</p>
				
				<h3 class="h3style text-center">5. Partner with the Albion Fisheries Research Centre (AFRC), Department of Fisheries and Rodrigues</h3>
				<p class="lead content">In parallel we are also considering setting up a voluntary conservation area in the lagoon of Anse La Raie. This project consists of several steps, namely community awareness on the importance of conservation and preservation of the marine environment, organizing events (film screening in open air, open day), eco guide training for 10 residents of in the area of Anse la Raie, numerous meetings with the community, hoteliers and key players in the lagoon fishermen.</p>
			
