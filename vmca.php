				<h2 class="text-center">What are VMCAs?</h2>
				<p class="lead content">Voluntary Marine Conservation Areas (VMCAs) are selected sites in the lagoon where resource users and coastal communities agree that no extractive or destructive activities will be done.  The primary objective is to help marine life and biodiversity of these VMCAs and the surrounding lagoon restore themselves. VMCAs can only be established with the support and approval of all fishermen registered at the nearest fish landing station to the chosen site.
The establishment of voluntarily protected sites will inevitably increase the area of marine resources protected in the lagoons and can ultimately lead to a network of protected areas in Mauritius. The success of these VMCA sites can help further promote real community co-management arrangements with local government for legally designated Marine Protected Areas and Marine Reserves.
Reef Conservation has been working with fishers and other local coastal community stakeholders to facilitate the establishment of VMCAs since 2009. There are currently to active VMCAs one in Roches Noires and one in Anse La Raie. With new funding, Reef will continue to work with these villages to protect the marine resources there.p>			
